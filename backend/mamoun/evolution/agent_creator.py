# Patterns that are FORBIDDEN in LLM-generated agent code for security
FORBIDDEN_CODE_PATTERNS = [
    r"os\.system\s*\(",
    r"subprocess\.",
    r"eval\s*\(",
    r"exec\s*\(",
    r"__import__\s*\(",
    r"open\s*\(.+['\"]w['\"]",
    r"shutil\.rmtree",
    r"os\.remove\s*\(",
    r"os\.unlink\s*\(",
    r"import\s+pickle",
    r"pickle\.loads?\s*\(",
    r"ctypes\.",
    r"socket\s*\(",
    r"requests\.(post|put|delete|patch)\s*\(",
    r"httpx\.(post|put|delete|patch)\s*\(",
]


def _sanitize_generated_code(code: str) -> str:
    """
    تنظيف الكود المولّد — Remove dangerous patterns from LLM-generated code.

    Returns:
        Sanitized code string with dangerous patterns removed/replaced.
    """
    for pattern in FORBIDDEN_CODE_PATTERNS:
        match = re.search(pattern, code)
        if match:
            logger.warning(f"Blocked forbidden pattern in generated code: {match.group()}")
            # Replace the dangerous call with a safe comment
            code = re.sub(pattern, f"# BLOCKED_UNSAFE_CALL  # was: {match.group()}", code)
    return code


