FALLBACK_EXECUTE_BODY_TEMPLATE = '''            # LLM was unavailable — using capability-based fallback logic
            result = {{
                "success": True,
                "task": task,
                "agent": self._config.name,
                "capabilities": {capabilities_list},
            }}

            # Dispatch based on task keywords and declared capabilities
            task_lower = task.lower()
            handled = False

            for cap in {capabilities_list}:
                if cap.lower() in task_lower:
                    handled = True
                    result["matched_capability"] = cap
                    result["action_taken"] = f"Processed task via {{cap}} capability"
                    logger.info(f"{{self._config.name}}: Matched capability '{{cap}}' for task")
                    break

            if not handled:
                # Attempt generic processing based on the most relevant capability
                if {capabilities_list}:
                    primary_cap = {capabilities_list}[0]
                    result["matched_capability"] = primary_cap
                    result["action_taken"] = f"Processed task using primary capability: {{primary_cap}}"
                    logger.info(f"{{self._config.name}}: Using primary capability '{{primary_cap}}'")
                else:
                    result["action_taken"] = "No specific capability matched — task acknowledged"
                    logger.info(f"{{self._config.name}}: No capability matched for task")

            # Include context data if provided
            if context:
                result["context_keys"] = list(context.keys())
                result["context_included"] = True'''


